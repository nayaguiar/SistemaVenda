
package Beans;

public class FuncionarioB {
    private int cod_funci;
    private String nome_funci;
    private String cargo;
    private String admissao;
    private double salario;

    public int getCod_funci() {
        return cod_funci;
    }

    public void setCod_funci(int cod_funci) {
        this.cod_funci = cod_funci;
    }

    public String getNome_funci() {
        return nome_funci;
    }

    public void setNome_funci(String nome_funci) {
        this.nome_funci = nome_funci;
    }

    public String getCargo() {
        return cargo;
    }

    public void setCargo(String cargo) {
        this.cargo = cargo;
    }

    public String getAdmissao() {
        return admissao;
    }

    public void setAdmissao(String admissao) {
        this.admissao = admissao;
    }

    public double getSalario() {
        return salario;
    }

    public void setSalario(double salario) {
        this.salario = salario;
    }
    
    
    
}
