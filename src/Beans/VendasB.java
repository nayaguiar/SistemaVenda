
package Beans;

public class VendasB {
    private int cod_vendas;
    private int cod_cliente;
    private int cod_funci;
    private String data_compra;
    private double valor_total;

    public int getCod_vendas() {
        return cod_vendas;
    }

    public void setCod_vendas(int cod_vendas) {
        this.cod_vendas = cod_vendas;
    }

    public int getCod_cliente() {
        return cod_cliente;
    }

    public void setCod_cliente(int cod_cliente) {
        this.cod_cliente = cod_cliente;
    }

    public int getCod_funci() {
        return cod_funci;
    }

    public void setCod_funci(int cod_funci) {
        this.cod_funci = cod_funci;
    }

    public String getData_compra() {
        return data_compra;
    }

    public void setData_compra(String data_compra) {
        this.data_compra = data_compra;
    }

    public double getValor_total() {
        return valor_total;
    }

    public void setValor_total(double valor_total) {
        this.valor_total = valor_total;
    }
    
    
    
}
